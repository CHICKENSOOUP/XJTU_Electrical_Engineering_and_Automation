/**
  * @brief This function handles TIM3 global interrupt.
  */
extern short *epmd;
void TIM3_IRQHandler(void)
{
  /* USER CODE BEGIN TIM3_IRQn 0  该部分内容可自由修改 */
static unsigned short LEDpwm;
	LEDpwm++;	LEDpwm%=9800;
	if(LEDpwm<4900)	TIM3->CCR2=			LEDpwm/2+88;
	else					TIM3->CCR2=4900-	LEDpwm/2+88;
	if(LEDpwm%10==1)	{	epmd[50]++;		epmd[46]|=0x000f;}
	if(LEDpwm%100==0)							{ epmd[49]++; epmd[46]|=0x00f0;	}				//Set a Click Flag

	if(!(GPIOE->IDR&0x08))															//PE2(down Key) push
	{	if(LEDpwm%8>3)	GPIOB->BSRR|=1<<5;	else 	GPIOB->BRR|=1<<5;	}	//Beep
//ISR 自行扩展Begin
		char it=0;
	while(it<8)
	{epmd[40+it]=epmd[40];
		if(it==0)
		{epmd[31]=epmd[30]/8;
			epmd[30]=0;}
		epmd[30]+=epmd[40+it];
			it++;}
		
		
//ISR 自行扩展END
	/* USER CODE END TIM3_IRQn 0 */
  HAL_TIM_IRQHandler(&htim3);
  /* USER CODE BEGIN TIM3_IRQn 1 */

  /* USER CODE END TIM3_IRQn 1 */
}

